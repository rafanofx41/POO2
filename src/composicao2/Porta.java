package composicao2;

public class Porta {
	
	private int height;
	private int widht;
	private String composition;
	public Porta(int height, int widht, String composition) {
		super();
		this.height = height;
		this.widht = widht;
		this.composition = composition;
	}
	public int getHeight() {
		return height;
	}
	public int getWidht() {
		return widht;
	}
	public String getComposition() {
		return composition;
	}
	
	

}
